 

public class Student {
    public String name;
    public int age ;
    public String address;

public Student(String name, int age, String address) {
    this.name = name;
    this.age = age;
    this.address = address;
}

public String getName() {
    return name;
}

public int getAge() {
    return age;
}

public String getAddress() {
    return address;
}
   
}
